function translateEl(element){
	langDict = jsonTranslationsEN;
	languageEl = document.getElementById("languageSelect");
	if (languageEl){
		if (languageEl.value === 'fr')
			langDict = jsonTranslationsFR;
		if (languageEl.value === 'es')
			langDict = jsonTranslationsES;
	}

	if (element.hasAttribute("data-innerText-translate"))
		element.innerText = langDict[element.getAttribute('data-innerText-translate')];
	if (element.hasAttribute("data-placeHolder-translate"))
		element.placeholder = langDict[element.getAttribute('data-placeHolder-translate')];
	if (element.hasAttribute("data-title-translate"))
		element.title = langDict[element.getAttribute('data-title-translate')];
}

function translateAll(){
	langDict = jsonTranslationsEN;
	languageEl = document.getElementById("languageSelect");
	if (languageEl){
		if (languageEl.value === 'fr')
			langDict = jsonTranslationsFR;
		if (languageEl.value === 'es')
			langDict = jsonTranslationsES;
	}

	innerTextElements = document.querySelectorAll('[data-innerText-translate]');
	if (innerTextElements){
		innerTextElements.forEach(function (element) {
			element.innerText = langDict[element.getAttribute('data-innerText-translate')];
		});
	}

	placeHolderElements = document.querySelectorAll('[data-placeHolder-translate]');
	if (placeHolderElements){
		placeHolderElements.forEach(function (element) {
			element.placeholder = langDict[element.getAttribute('data-placeHolder-translate')];
		});
	}

	titleElements = document.querySelectorAll('[data-title-translate]');
	if (titleElements){
		titleElements.forEach(function (element) {
			element.title = langDict[element.getAttribute('data-title-translate')];
		});
	}

	guestElements = document.querySelectorAll('[data-guest-translate]');
	if (guestElements){
		guestElements.forEach(function (element) {
			element.innerText = `${langDict["You are a guest of "]}${element.getAttribute('data-guest-translate')}${langDict["'s tournament"]}`;
		});
	}

	positionElements = document.querySelectorAll('[data-position-translate]');
	if (positionElements){
		positionElements.forEach(function (element) {
			str = element.getAttribute('data-position-translate').split(" ");
			element.innerText = `${langDict["You are in position"]} ${str[0]} / ${str[1]} (${str[2]} ${langDict["missing"]})`
		});
	}

	streakElements = document.querySelectorAll('[data-streak-translate]');
	if (streakElements){
		streakElements.forEach(function (element) {
			str = element.getAttribute('data-streak-translate').split(" ");
			element.innerText = `${str[0]} ${langDict[str[1]]}`
		})
	}
}

document.addEventListener("DOMContentLoaded", translateAll);

function changeLanguage(value){
	sendDataToSocket('changeLanguage', {value : value});
	translateAll();
}

function setUserLanguage(data){
	if (data['lang'] === '')
		data['lang'] = 'en';
	selectEl = document.getElementById("languageSelect");
	if (selectEl)
		selectEl.value = data['lang'];
	translateAll();
}
