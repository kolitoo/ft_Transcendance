function compareJSONKeys(json1, json2, name1, name2) {
    const keys1 = Object.keys(json1);
    const keys2 = Object.keys(json2);

    const missingKeys = keys1.filter(key => !keys2.includes(key));
    const addedKeys = keys2.filter(key => !keys1.includes(key));

    if (missingKeys.length > 0) {
        console.log(`Keys present in ${name1} but missing in ${name2}:`);
        missingKeys.forEach(key => console.log(key));
    }

    if (addedKeys.length > 0) {
        console.log(`Keys present in ${name2} but missing in ${name1}:`);
        addedKeys.forEach(key => console.log(key));
    }

    if (missingKeys.length === 0 && addedKeys.length === 0) {
        console.log("Both JSON objects have the same keys.");
    }
}

